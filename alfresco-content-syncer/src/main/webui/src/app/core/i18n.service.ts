@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly storageKey = 'syncer.locale';
  private readonly dictionaries: Record<Locale, Dictionary> = { en: EN, it: IT };
  private activeLocale: Locale = this.readLocale();

  get locale(): Locale {
    return this.activeLocale;
  }

  setLocale(locale: Locale): void {
    this.activeLocale = locale;
    localStorage.setItem(this.storageKey, locale);
  }

  t(key: string, params: Record<string, string | number> = {}): string {
    let value = this.dictionaries[this.activeLocale][key] ?? EN[key] ?? key;
    Object.entries(params).forEach(([paramKey, paramValue]) => {
      value = value.replaceAll(`{${paramKey}}`, String(paramValue));
    });
    return value;
  }

  private readLocale(): Locale {
    const stored = localStorage.getItem(this.storageKey);
    return stored === 'it' ? 'it' : 'en';
  }
}
